const month = 'may'

switch (month) {
    case 'jan':
        console.log('January');
        break;
    case 'feb':
        console.log('Fabruary');
        break;
    case 'march':
        console.log('March');
        break;
    case 'april':
        console.log('April');
        break;
    case 'may':
        console.log('May');
        break;

    default:
        console.log("Invalid Month");
        break;
}